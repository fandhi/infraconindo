            <h4>Aspek Keamanan Menara BTS</h4>
            <span class="date_news">01/01/1970</span>
        </div>
        <p><i>Keberadaan menara BTS kerap mengundang polemik di masyarakat. Meski kerap menuai kritik tapi hadirnya menara juga sangat dibutuhkan untuk peningkatan kualitas jaringan komunikasi. 
            Untuk itu masyarakat perlu mengetahui aspek-aspek dasar keamanan pada menara BTS.</i></p>

        <p>Berita seputar penolakan warga atas kehadiran menara BTS (base transceiver station) kerap terdengar di beberapa surat kabar dan televisi. 
            Umumnya yang menjadi keluhan warga adalah belum adanya ijin pembangunan dari penduduk sekitar, ini membuat langkah operator ikut tersendat dalam menggelar jaringan. 
            Dalam implementasi di lapangan, operator selular umumnya menggunakan jasa kontraktor untuk keperluan akuisisi site. Penolakan warga ini dapat dikelompokkan pada segi persoalan teknis dan non teknis.</p>

        <p>Persoalan teknis umumnya menyangkut ke khawatiran warga pada efek radiasi dan bahaya rubuhnya tiang menara. 
            Sedang persoalan non teknis biasanya berkaitan dengan masalah kesepakatan jual beli dan kepemilikan lahan. Namun dalam tulisan ini hanya dibahas seputar persoalan teknis. 
            Perlu dipahami terlebih dahulu, guna menggelar kualitas layanan telekomunikasi selular yang memadai, keberadaan elemen menara dan antena BTS mau tidak mau memang dibutuhkan. 
            Seiring bertambahnya jumlah pelanggan suatu operator di suatu wilayah, peningkatan kapasitas cell menjadi hal yang mutlak. 
            Jika ini tak diantisipasi sering munculnya drop call di jam-jam sibuk tidak bisa dihindari lagi, pada akhirnya toh merugikan kepentingan pelanggan sendiri. 
            Kian padatnya jumlah pelanggan artinya membawa konsekwensi bertambahnya jumlah menara, contohnya di kota besar seperti Jakarta.</p>

        <p>Menjamurnya jumlah menara BTS membawa aneka dampak psikologis dan sosial, meski sudah ada opsi penggabungan menara beberapa operator, problem di masyarakat tetap kerap muncul. 
            Untuk itu perlu diketahui aspek-aspek dasar keamanan dalam pembangunan dan gelaran menara BTS. Beberapa aspek tersebut adalah</p>


        <p><b>1. Lokasi</b></p>


        <p>Untuk optimalisasi jaringan, operator perlu memberikan jarak yang konsisten antar BTS, semisal per 1,5 kilometer. 
            Tentunya masalah jarak terkait dengan kepadatan trafik pelanggan di suatu daerah. Umumnya di perkotaan yang padat pemukiman, operator lebih sulit untuk menciptakan jarak yang konsisten antar BTS. 
            Ini disebabkan tingkat kesulitan untuk mendapat lahan tanah (green filed) yang pas.
            Untuk menyiasati persoalan lahan, solusinya adalah gelar menara BTS di atas gedung bertingkat (roof top). 
            Sebagai informasi saat ini Pemerintah Daerah DKI Jakarta sudah melarang pembangunan menara baru BTS di green field.</p>


            <p><b>2. Desain Menara</b></p>


        <p>Desain menara BTS tentu tidak selalu sama, disesuaikan dengan kebutuhan dan kondisi geografis di wilayah bersangkutan. 
            Diantara pertimbangan dalam desain yakni faktor beban menara, kekuatan angin dan kondisi tanah yang kesemuanya harus memenuhi safety margin yang telah disyaratkan ITU (International Telecommunication Uinion). 
            Civil engineer dari kontraktor terlibat penuh dalam hal untuk keperluan IMB (Ijin Mendirikan Bangunan).</p>

        <p>Faktor beban menara yakni prediksi pemakaian perangkat hardware yang ditempatkan di atas tower. Semisal tower yang hanya ditempati tiga antena Trx dan microwave, tentu tidak memerlukan menara rangka tinggi. 
            Namun umumnya operator sudah menyiapkan beban menara untuk penambahan beberapa perangkat untuk kebutuhan kedepan, contohnya beban tambahan hardware 3G.</p>

        <p>Lalu faktor kekuatan angin menjadi hal penting pula, sebelum membangun menara operator telah mendapat informasi kecepatan angin oleh BMG (Badan Meteorologi dan Geofisika). 
        Di daerah dengan kondisi kecepatan angin tinggi biasanya digunakan tipe tower rangka segi empat atau segi tiga. Umumnya wilayah dengan kecepatan angin tinggi seperti di ladang persawahan dan pesisir pantai. 
        Kemudian kondisi dan jenis tanah turut diperhitungkan, ini menyangkut kedalaman tiang pancang. Kualitas menara harus benar-benar kuat dan tahan terhadap gempa. 
        “Seperti pada gempa Yogya bulan Mei tahun lalu, tidak satu pun menara BTS kami yang runtuh,” ujar M. Buldansyah, Direktur Network Operations XL.</p>


        <p><b>3. Radiasi</b></p>


        <p>Soal radiasi paling populer untuk “menggugat” keberadaan menara BTS. Sebagaimana layaknya perangkat elektronik, menara BTS memang memancarkan radiasi. 
            Menjadi ketakutan orang umumnya isu radiasi yang bisa membuat mandul. Padahal radiasi yang dipancarkan dari perangkat microwave terbilang kecil, kadarnya pun tak lebih besar dari radiasi yang ditimbulkan sebuah ponsel. 
            Ditambah lagi penempatan hardware berada di ketinggian sehingga tidak berdampak buruk bagi kesehatan. Dalam hal ini operator dan kontraktor mutlak mengadakan sosialisasi.</p>


